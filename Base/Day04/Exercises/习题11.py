# 字符翻译程序
#  * 查看笔记本键盘1-9还有0号键其上方的字符，要求用户输入"1"，那么输出"!",输入"2"，那么输出"@",以此类推
#  * 用字典完成这个任务
#  * 用户如果输入的字符长度超过1或者是除数字以外其他字符，提示"未收录该字符的含义，请重新输入"


num_dict = {'1':'!','2':'@','3':'#','4':'$','5':'%','6':'^','7':'&','8':'*','9':'(','0':')'}
# 未收录该字符的含义，请重新输入
while True:
    num = input('请输入数字：')
    if num not in num_dict or len(num)>1 :
        print('未收录该字符的含义，请重新输入')
    else:
        print('数字 %s对应的字符为 %s' % (num,num_dict[num]))
        print('-'*80)
